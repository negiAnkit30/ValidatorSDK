    func query() {
        let input = "Wednesday,Friday,Sunday,Monday,Tuesday"

        // Splitting the input into an array
        var inputArray = input.components(separatedBy: ",")

        // Defining a dictionary to assign numeric values to the days of the week
        let dayValues: [String: Int] = [
            "Sunday": 0,
            "Monday": 1,
            "Tuesday": 2,
            "Wednesday": 3,
            "Thursday": 4,
            "Friday": 5,
            "Saturday": 6
        ]

        // Sorting the array based on the numeric values
        inputArray.sort { dayValues[$0]! < dayValues[$1]! }

        // Joining the sorted array elements into a string
        let output = inputArray.joined(separator: ",")

        print(output) // Output: Sunday,Monday,Tuesday,Wednesday,Friday
    }

